import java.util.Scanner;

/**
 * @author agupt316
 *
 */
public class MinDraws {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		int t;
		int pairs;
		t = in.nextInt();
		for(int i=0;i<t;i++){
			pairs = in.nextInt();
			System.out.println(pairs+1);
			pairs = 0;
		}
	}

}
